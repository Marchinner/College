<?php
// Declaring a empty array
$cars = array();

// First declaration form and values atribuition
$cars = Array("Fusca", "Monza", "Passat");

// Second declaration form
// Similar to the first, but is like JavaScript notation
$cars = ["Fusca", "Monza", "Passat"];

// Third form
$cars[0] = "Fusca";
$cars[1] = "Monza";
$cars[2] = "Passat";